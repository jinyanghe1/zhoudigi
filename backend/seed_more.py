def get_dynasty_id(db, dynasty_name):
    dynasty = db.query(Dynasty).filter(Dynasty.name == dynasty_name).first()
    if dynasty:
        return dynasty.id
    for d in db.query(Dynasty).all():
        if dynasty_name in d.name or d.name in dynasty_name:
            return d.id
    return 1


def get_or_create_author(db, name, dynasty_id, bio=""):
    author = db.query(Author).filter(Author.name == name).first()
    if author:
        return author.id
    author = Author(name=name, dynasty_id=dynasty_id, bio=bio)
    db.add(author)
    db.commit()
    db.refresh(author)
    return author.id


def add_articles():
    db = SessionLocal()
    added = 0
    skipped = 0

    for data in MORE_ARTICLES:
        existing = db.query(Article).filter(Article.title == data["title"]).first()
        if existing:
            skipped += 1
            print(f"跳过（已存在）: {data['title']}")
            continue

        dynasty_id = get_dynasty_id(db, data["dynasty"])
        author_id = get_or_create_author(db, data["author"], dynasty_id)

        article = Article(
            title=data["title"],
            author_id=author_id,
            dynasty_id=dynasty_id,
            content=data["content"],
            translation=data.get("translation", ""),
            category=data.get("category", ""),
            is_selected=False,
            read_count=0,
            word_count=len(data["content"]),
            source=data.get("source", "维基文库"),
            selection_reason=data.get("selection_reason", "")
        )
        db.add(article)
        added += 1
        print(f"添加: {data['title']} - {data['author']}")

    db.commit()
    print(f"\n完成！新增 {added} 篇，跳过 {skipped} 篇")
    print(f"当前: {db.query(Article).count()} 篇文章")
    db.close()


if __name__ == "__main__":
    add_articles()
